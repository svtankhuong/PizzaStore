    public void xulynutxoa() {
        int row = tblKhachHang.getSelectedRow(); // Lấy dòng đang chọn
        if (row == -1) {
            new MyDialog("Vui lòng chọn nhân viên để xóa!", MyDialog.ERROR_DIALOG);
            return;
        }
        String maKHchu = txtMaKH.getText().trim();
        int maKH = Integer.parseInt(maKHchu);
        int confirm = JOptionPane.showConfirmDialog(
                null,
                "Bạn có chắc chắn muốn xóa nhân viên này?",
                "Xác nhận",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {
            khbus.XoaKhachHang(maKH);
            loadtablekhachhang();
        }
    }

    public void xuLyLocKhachHangTheoChiTieu() {
        try {
            long min = Long.parseLong(txtMin.getText().trim());
            long max = Long.parseLong(txtMax.getText().trim());

            ArrayList<KhachHangDTO> danhSachLoc = khbus.locKhachHangTheoChiTieu(min, max);
            DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
            // Xóa dữ liệu cũ trong bảng
            model.setRowCount(0);

            // Đổ dữ liệu mới vào bảng
            for (KhachHangDTO kh : danhSachLoc) {
                model.addRow(new Object[]{
                    kh.getMaKH(),
                    kh.getHoDem(),
                    kh.getTen(),
                    kh.getSdt(),
                    kh.getDiaChi(),
                    kh.getTongChiTieu()
                });
            }
            tblKhachHang.setModel(model);
            TableCustomizer.customize(tblKhachHang);
        } catch (NumberFormatException e) {
            new MyDialog("Vui lòng nhập số hợp lệ!", MyDialog.ERROR_DIALOG);
        }
    }

