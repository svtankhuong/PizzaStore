public void loadtablekhachhang() {
        String[] columnNames = {"Mã KH", "Họ đệm", "Tên", "Số điện thoại", "Địa chỉ", "Tổng chi tiêu"};
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);

        ArrayList<KhachHangDTO> dsKH = khbus.getDanhSachKhachHang();
        for (KhachHangDTO kh : dsKH) {
            Object[] row = {
                kh.getMaKH(),
                kh.getHoDem(),
                kh.getTen(),
                kh.getSdt(),
                kh.getDiaChi(),
                kh.getTongChiTieu()
            };
            model.addRow(row);
        }
        tblKhachHang.setModel(model);
        TableCustomizer.customize(tblKhachHang);
    }

    public void xulynuttimkiem() {
        String tukhoa = txtSearch.getText().trim();
        if (tukhoa.isEmpty()) {
            loadtablekhachhang();
            return;
        }
        ArrayList<KhachHangDTO> dsKH = khbus.timKiemKhachHang(tukhoa);
        String[] columnNames = {"Mã NV", "Họ đệm", "Tên", "Ngày sinh", "Giới tính"};
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);
        for (KhachHangDTO kh : dsKH) {
            Object[] row = {
                kh.getMaKH(),
                kh.getHoDem(),
                kh.getTen(),
                kh.getSdt(),
                kh.getDiaChi(),
                kh.getTongChiTieu()
            };
            model.addRow(row);

        }
        tblKhachHang.setModel(model);
        TableCustomizer.customize(tblKhachHang);
    }

    public void hienthongtinkhachtutable() {
        int row = tblKhachHang.getSelectedRow();
        String MaKH = tblKhachHang.getValueAt(row, 0).toString();
        String HoLot = tblKhachHang.getValueAt(row, 1).toString();
        String Ten = tblKhachHang.getValueAt(row, 2).toString();
        String SoDienThoai = tblKhachHang.getValueAt(row, 3).toString();
        String DiaChi = tblKhachHang.getValueAt(row, 4).toString();
        String TongChiTieu = tblKhachHang.getValueAt(row, 5).toString();

        txtMaKH.setText(MaKH);
        txtHoLot.setText(HoLot);
        txtTen.setText(Ten);
        txtSDT.setText(SoDienThoai);
        txtDiaChi.setText(DiaChi);
        txtTongChiTieu.setText(TongChiTieu);
    }

